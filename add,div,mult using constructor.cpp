#include <iostream>
using namespace std;

int main()
{
  double firstNumber, secondNumber;

  cout << "Enter the first number: " << endl;
  cin >>firstNumber;

  cout << "Enter the second number: " << endl;
  cin >>secondNumber;

  cout << "\nfirstNumber + secondNumber = " << firstNumber + secondNumber << endl;

  cout << "\nfirstNumber - secondNumber = " << firstNumber - secondNumber << endl;

  cout << "\nfirstNumber * secondNumber = " << firstNumber * secondNumber << endl;

  cout << "\nfirstNumber/secondNumber = " << firstNumber / secondNumber << endl;
};
